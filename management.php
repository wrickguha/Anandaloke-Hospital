<h3 class="font-title">Anandaloke Hospital : The Dream Project</h3>
<p class="history-ct">Anandaloke Hospital has traced an impressive growth story from a 25-bed centre to a 100-bedded multi-speciality hospital serving the healthcare needs of the people in East India.</p>

<h5 class="font-title apt-20">The 'Dream' project</h5>
<p class="history-ct">
<img src="images/dream.jpg" alt="Dream" class="dream"/>
Born in a small town in Bangladesh, Dr. Sushanta Kumar Roy, a noted radiologist did his Bachelor's Degree in Medicine from Dhaka Medical College, DTCT from Kolkata Medical College and then his Master's Degree from Kasturba Medical College.
<br><br>
He started his career in 1993 as a Consultant Radiologist at a local nursing home in Siliguri. Later, inspired by his zeal and enthusiasm, he started his own ultrasonography (USG) clinic. After three years, he expanded this USG clinic to a diagnostic clinic with a laboratory and radiological facility along with endoscopy.
<br><br>
At present, this diagnostic centre is known as the 'Anandaloke Sonoscan Centre'. Equipped with 1.5 Tesla MRI, 16-slice CT scan and enteroscope, it boasts of an annual turnover of Rs four crores.
<br><br>
In 2003, he took up a brownfield project and started 'Anandaloke Hospital & Neurosciences Centre' with 25 beds. Today, that centre has grown up and expanded to a 100-beded multispecialtiy hospital with 500 employees enjoying an occupancy rate of 95 per cent with an annual turnover of Rs 16.5 crores.
<br><br>
Besides, neurosurgery, neurology, gastroenterology, nephrology, urosurgery, plastic surgery, trauma, cardiology and orthopaedics, eye, ENT, internal medicine, general surgery, dermatology, super-speciality and speciality services are also offered at this hospital. The hospital is recognised as a Class-I service provider and it is ISO certified as well.
</p>

<h5 class="font-title apt-20">A Visionary</h5>

<p class="history-ct">Dr. Roy, the Managing Director of the hospital realised the importance of trained manpower and started 'Anandaloke School of Nursing' and 'Anandaloke Paramedical Institute'. At present, 25 GNM and 17 paramedics are trained here every year.</p>

<h5 class="font-title apt-20">Reaching out to the underprivileged</h5>

<p class="history-ct">Dr Roy did not forget his social responsibility and he developed a trust called the 'Uma Suresh Memorial Charitable Trust' in the name of his parents. Besides other social services, regular surgery for cleft lip and palate are performed here, by the plastic surgeon, under the 'Smile Train Project'.</p>

<h5 class="font-title apt-20">Road Ahead</h5>

<p class="history-ct">A workaholic and fitness freak, Dr. Roy said that his mantra for success is his hard work and belief in his dreams.</p>

