<h3 class="font-title">Who We Are?</h3>
<p class="history-ct">Our Journey started  in 2003 with 25 beds and built up area of  45000sqft  with around 10 departments.We have now 150 licensed  beds and capacity of 180 beds with built up area of 1 Lakh sqft with  around 25 departments. Around 50% area is under centralized air conditioning.
	<br>
	<br>
We have largest haemodialysis unit in this region with 38 state of art machine and double RO plant.Our Nephrology department  is also performing CAPD. Our Newly developed cardiac centre  is performing primary angioplasty,CABG,and other cardiovascular surgeries.
	<br>
	<br>
At present It offers superspeciality services in neuromedicine, neurosurgery, plastic surgery, gastroenterology, nephrology, urosurgery,cardiology & cardiothoracic. It is managed by a team of around 500 committed professionals: administrators, team of doctors including superspecialist, specialist, junior doctors, nursing professionals, paramedical staffs.
	<br>
	<br>
Hospital has risk management department and implementing 10 JCI Safe health care goals, monitoring safety indicators and bench marking with best in health industry.
	<br>
	<br>
<!--It is NABH certified,-->
It is ISO certified and Class-I service provider of West Bengal Health Scheme. It is also preferred service of Medical insurance company and TPA. Anandaloke is empaneled by NF Railway, Sikkim Government Health Scheme, UCO Bank, NHPC, PHPA ( Joint Venture of Bhutan & India). It treats more than 8000 indoor patients per year and more than 70000 OPD patients per year. 
	<br>
	<br>
Several Safety Goals were adopted like improved accuracy of patient identification, reduce risk of HAI, improve safety of drug administration, prevent pressure ulcer, prevention of sudden death, improve effectiveness of communication amongst care giver, prevent fall in hospital, reconciliations of medicine at discharge, prevention of suicides, prevention of risk associated with blood transfusion, safe transport of patients, reduce TAT of critical values, sentinel events reporting, etc.
	<br>
	<br>
Top management support these risk management programmes and all stake holders are trained, aligned, monitored to ensuure goals are achieved. Results are compared with benchmarks of industry.
	<br>
	<br>
Hospital is operating a Nursing school and College  under West Bengal Nursing Council recognized by Indian Nursing Council. Paramedical courses: Diploma in critical care, Dialysis Technician, OT-Technician, Radiology Technician, Laboratoy Technician under State Medical Council are also provided.
	<br>
	<br>
Dr .A. K. Khandelwal<br>
Vice President & Medical Director
</p>

